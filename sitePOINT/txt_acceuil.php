	<H4>1. Comment nous est venu l’idée?</H4>
	<p>
	Ce projet a duré un total de 9 mois, donc d’octobre 2020 à juin 2021. La thématique et le but de ce projet a été une idée générale du groupe, nous voulons à tout prix aider les infirmiers et infirmières qui travaillent énormément et souffrent. A travers, ce projet nous avons coder notre robot avec le langage Arduino, nous avons utiliser blender pour le maquettes à faire ensuite sur une imprimante 3D, l’HTML, le php et le CSS pour le site que vous voyez en ce moment ainsi toute les parties communication ainsi que la partie commercialisation de notre produit. Ce projet nous a appris à avoir beaucoup plus de responsabilités et de maturité pour maintenant et dans les années avenir.
	</p>
</div>

<div>
	<H4>2. But du robot</H4>
	<p>
	Le but du robot est d’arriver de manière autonome jusqu'à un point déterminé puis de monter le plateau en hauteur puis de l’avancer jusqu’à un potentiel patient. Il le fait en suivant une ligne noire marquée au sol. Pour l’instant rudimentaire, on peut imaginer que dans le futur, il soit équipé d’un capteur ultra son pour repérer et éviter les personnes qu’il serait amené à rencontrer, ou bien encore ajouter une base de données et une interface pour permettre de programmer les chambres ayant besoin de ces services.
	</p>
