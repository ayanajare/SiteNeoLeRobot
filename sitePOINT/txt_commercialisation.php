        -Une carte Arduino qui va jouer le rôle de cerveau à notre prototype, il va servir à connecter les différents câbles et aussi à injecter notre code Arduino. Son prix est de 24 euros.<br>
        -Les câbles dont nous avons besoin pour relier les composants à la carte Arduino. Nous allons prendre un lot de câbles femelle, mâle et mâle femelle pour le prix de 5,50 euros.<br>
