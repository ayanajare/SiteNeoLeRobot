	<H4>1. Evolution technique du robot</H4>
	<img src="./img/img_test_text_center.jpg" alt="Image du robot" class="imgDroite">
	<p>
		De part l’expérience du groupe, nous avons rapidement eu une première liste des pièces nécessaires pour faire avancer un robot et lui faire bouger un plateau tout en suivant une ligne pour se repérer. Un premier design a vite été établi a l’aide de clip studio.<br>
		Après avoir choisi le châssis sur internet et vu les pièces disponibles, nous nous sommes rendus compte que les pièces s’adaptaient mal au design d’origine. Nous sommes donc repartis sur clip studio pour faire un deuxième modèle.<br>
		En particulier, un élément a beaucoup changer est la façon dont on tend le plateau, nous sommes d’abord partis d’un design qui ressemble a un bras à un design qui repose sur un jeu de deux cerveau moteurs.
	</p>
