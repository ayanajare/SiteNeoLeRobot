<div>
	<H4>2. Evolution graphique du robot</H4>
	<p>
		A la fin de la conception technique du robot, nous avions quelque chose qui pouvais fonctionner mais pour cela nous aurions besoin d’utiliser une imprimante 3D pour faire certaines pièces.<br>
		Nous avons donc utiliser blender pour designer.<br>
		Pour ne pas se tromper sur les dimensions, nous avons d’abord fait un modèle de toutes les dimensions du robot pour être sur de ne pas se tromper. Nous sommes aussi passés par l’impression 3D d’une partie des pièces avant de les imprimer en entier. Avec toutes ces précautions, nous avons pu créé des pièces sur mesure pour notre robot.
	</p>
