<div>
	<H4>1. Defis du passage a taille réelle</H4>
	<p>
	Le code devrait changer sur un modèle a taille réelle. Par exemple, avec un robot plus grand, on doit demander d'étendre plus le plateau.<br>
	Proportionnellement, il serait plus haut et moins large par rapport a notre modèle réduit.<br>
	Les pièces composant le robot seraient des version plus cher et performantes mais pas nécessairement différentes.
	Il fonctionnerai sur le même principe et aura juste besoin de souenir et de déplacer plus de poids.
	</p>
</div>

<div>
	<H4>2. Axes d’améliorations</H4>
	<p>
		Nous avions penser a rajouter un écran pour avoir une forme d’interactivité avec le patient.<br>
		Nous avons également conscience que en l'état, le robot ne serait pas vraiment utilisable.
		En effet nous voudrions rajouter par la suite des capteurs pour l'empécher de faire des colisions avec des personnes marchant dans les couloirs.<br>
		Une autre amélioration a laquelle nous avions pensée est la possibilité pour le robot de lire dans une base de données quelles chambre il doit servir et dans quel ordre.
	</p>
